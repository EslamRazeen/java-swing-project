    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        
        try {
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/myproject" , "root" , "");
            
            PreparedStatement ps = con.prepareStatement("delete from mobiles where ssn=?");
            
            DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
            
           
            
            if(jTable2.getSelectedRowCount() == 1){
                ps.setString(1, model.getValueAt(jTable2.getSelectedRow(), 2).toString());
                ps.executeUpdate();

                model.removeRow(jTable2.getSelectedRow());

                reqModel.setText("");
                
                reqCount.setText(String.valueOf(model.getRowCount()));
                PreparedStatement ps4 = con.prepareStatement("update results set requests=? where id=1");
                ps4.setInt(1, model.getRowCount());
                ps4.executeUpdate();


                PreparedStatement ps2 = con.prepareStatement("select * from results where id=1");

                ResultSet rs2 = ps2.executeQuery();
                
                if(rs2.next()){
                    
                    int count = rs2.getInt("checked");

                    count++;
                    reqChecked.setText(String.valueOf(count));
                    ps2 = con.prepareStatement("update results set checked=? where id=1");
                    ps2.setInt(1, count);
                    ps2.executeUpdate();
                
                    
                }

            }
            

            
            
        } catch (Exception ex) {
            ex.printStackTrace();
        }
        
        
    }//GEN-LAST:event_jButton5ActionPerformed

