    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
        
        
        try {
            Class.forName("com.mysql.jdbc.Driver");
            
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/myproject" , "root" , "");
          
            String user = adminName.getText();
            String email= adminEmail.getText();
            String pass = adminPassword.getText();
            int age = Integer.parseInt(adminAge.getText());
            
            
            PreparedStatement p = con.prepareStatement("select * from accounts where email=? and pass=?");
            
            String Aemail = adminEmail.getText();
            String Apass = adminPassword.getText();
            
            p.setString(1,Aemail );
            p.setString(2, Apass);
            
            ResultSet rss = p.executeQuery();
            
            if(rss.next()){
                JOptionPane.showMessageDialog(this, "This Account Already Exists");
            }
            else{
                
                PreparedStatement ps = con.prepareStatement("insert into accounts values(?,?,?,?,?)");

                ps.setString(1, user);
                ps.setString(2, email);
                ps.setString(3, pass);
                ps.setInt(4, age);
                ps.setString(5, "Member");

                ps.executeUpdate();

                adminName.setText("");
                adminEmail.setText("");
                adminPassword.setText("");
                adminAge.setText("");

                inserted.setText("Account inserted");
                
                DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
                
                Object [] data = {user , email , pass , age};
                model.addRow(data);
                
                
            }
            
            
            
            
            
            
        } catch (Exception ex) {
            ex.printStackTrace();
        }
        
        
        
        
    }//GEN-LAST:event_jButton2ActionPerformed

